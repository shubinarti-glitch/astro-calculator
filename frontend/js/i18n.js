let LANG = (function () {
  try {
    return localStorage.getItem("astro_lang") || "ru";
  } catch (e) {
    return "ru";
  }
})();

function t(key) {
  return (I18N[LANG] && I18N[LANG][key]) || (I18N.ru[key] != null ? I18N.ru[key] : key);
}

function applyI18n() {
  document.documentElement.setAttribute("lang", LANG);
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const val = t(el.getAttribute("data-i18n"));
    if (el.children.length === 0) {
      el.textContent = val;
    } else {
      // элемент содержит вложенные узлы (напр. label с input) — меняем только первый текст
      let node = el.firstChild;
      while (node && node.nodeType !== 3) node = node.nextSibling;
      if (node) node.nodeValue = val;
      else el.insertBefore(document.createTextNode(val), el.firstChild);
    }
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.getAttribute("data-i18n-html"));
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.setAttribute("title", t(el.getAttribute("data-i18n-title")));
  });
}

function setLang(lang) {
  LANG = lang === "en" ? "en" : "ru";
  try {
    localStorage.setItem("astro_lang", LANG);
  } catch (e) {}
  applyI18n();
  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang: LANG } }));
}
